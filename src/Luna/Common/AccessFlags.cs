namespace Alluseri.Luna;

public enum ClassAccessFlags : ushort {
	///	<summary>Declared public; may be accessed from outside its package.</summary>
	ACC_PUBLIC = 0x0001,
	/// <summary>Declared final; no subclasses allowed.</summary>
	ACC_FINAL = 0x0010,
	/// <summary>Treat superclass methods specially when invoked by the invokespecial instruction.</summary>
	ACC_SUPER = 0x0020,
	/// <summary>Is an interface, not a class.</summary>
	ACC_INTERFACE = 0x0200,
	/// <summary>Declared abstract; must not be instantiated.</summary>
	ACC_ABSTRACT = 0x0400,
	/// <summary>Declared synthetic; not present in the source code.</summary>
	ACC_SYNTHETIC = 0x1000,
	/// <summary>Declared as an annotation interface.</summary>
	ACC_ANNOTATION = 0x2000,
	/// <summary>Declared as an enum class.</summary>
	ACC_ENUM = 0x4000,
	/// <summary>Is a module, not a class or interface.</summary>
	ACC_MODULE = 0x8000
}
public enum FieldAccessFlags : ushort {
	///	<summary>Declared public; may be accessed from outside its package.</summary>
	ACC_PUBLIC = 0x0001,
	/// <summary>Declared private; accessible only within the defining class and other classes belonging to the same nest.</summary>
	ACC_PRIVATE = 0x0002,
	///	<summary>Declared protected; may be accessed within subclasses.</summary>
	ACC_PROTECTED = 0x0004,
	///	<summary>Declared static.</summary>
	ACC_STATIC = 0x0008,
	///	<summary>Declared final; never directly assigned to after object construction.</summary>
	ACC_FINAL = 0x0010,
	///	<summary>Declared volatile; cannot be cached.</summary>
	ACC_VOLATILE = 0x0040,
	///	<summary>Declared transient; not written or read by a persistent object manager.</summary>
	ACC_TRANSIENT = 0x0080,
	///	<summary>Declared synthetic; not present in the source code.</summary>
	ACC_SYNTHETIC = 0x1000,
	///	<summary>Declared as an element of an enum class.</summary>
	ACC_ENUM = 0x4000
}
